## Příznaky:
Rychlé poškození krevních cév, což způsobuje vnitřní krvácení, těžkou anémii a selhání orgánů. Tmavá, téměř černá krev je výraznou vlastností.

## Příčiny:
Přenáší se kousnutím infikovaných tvorů nebo kontaktem s kontaminovanou krví. Běžné v oblastech, kde převládá temná magie.

## Léčba:
Magická léčivá kouzla zaměřená na čištění krve mohou zpomalit postup. Je známo, že výtažky z květu Silvermoon, který se nachází v posvátných zahradách, působí proti účinkům.