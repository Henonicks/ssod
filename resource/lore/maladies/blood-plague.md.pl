## Objawy:
Gwałtowne pogorszenie stanu naczyń krwionośnych, powodujące krwawienie wewnętrzne, ciężką niedokrwistość i niewydolność narządów. Ciemna, prawie czarna krew jest charakterystyczną cechą.

## Powoduje:
Przenoszony przez ukąszenia zakażonych stworzeń lub przez kontakt ze skażoną krwią. Powszechne w regionach, w których dominuje mroczna magia.

## Zabiegi:
Magiczne zaklęcia lecznicze skupiające się na oczyszczaniu krwi mogą spowolnić postęp. Wiadomo, że ekstrakty z kwiatu Silvermoon, znalezione w świętych ogrodach, przeciwdziałają skutkom.