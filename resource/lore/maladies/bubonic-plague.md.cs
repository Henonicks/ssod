## Příznaky:
Oteklé, bolestivé lymfatické uzliny (buboes), vysoká horečka, zimnice a únava. V těžkých případech vede k septikémii a selhání orgánů.

## Příčiny:
pronášely kousnutím infikovaných blech, které často napadají hlodavce. Běžné v přeplněných městských oblastech a regionech se špatnou hygienou.

## Léčba:
Očistná kouzla mohou pomoci v boji s infekcí. Předpokládá se, že vzácná bylina Starpetal, která se vyskytuje v odlehlých horských oblastech, má silné antibakteriální vlastnosti.