## Objawy:
Obrzęk, bolesne węzły chłonne (dymie), wysoka gorączka, dreszcze i zmęczenie. W ciężkich przypadkach prowadzi do posocznicy i niewydolności narządów.

## Powoduje:
przenoszona przez ukąszenia zakażonych pcheł, które często atakują gryzonie. Powszechne w zatłoczonych obszarach miejskich i regionach o złych warunkach sanitarnych.

## Zabiegi:
Zaklęcia oczyszczające mogą pomóc w walce z infekcją. Uważa się, że rzadkie zioło Starpetal, występujące w odległych regionach górskich, ma silne właściwości antybakteryjne.