## Příznaky:
Postižení jedinci pociťují neduživý zelený odstín kůže, doprovázený postupným rozkladem masa. Častá je únava, slabost a celková malátnost.

## Příčiny:
Stáhlo se působením určitých hub, které se nacházejí ve vlhkých jeskyních a bažinatých oblastech. Spory uvolňované houbami jsou primárním způsobem přenosu.

## Léčba:
Zkušený léčitel může použít očistná kouzla k vyčištění těla od plísňové infekce. Vzácné byliny nalezené v mystických hájích mohou být použity v obkladech na podporu zotavení.