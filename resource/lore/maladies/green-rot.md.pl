## Objawy:
Osoby dotknięte chorobą odczuwają chorobliwie zielony odcień skóry, któremu towarzyszy stopniowy rozkład ciała. Często występuje zmęczenie, osłabienie i ogólne złe samopoczucie.

## Powoduje:
Zarażony przez narażenie na niektóre grzyby występujące w wilgotnych jaskiniach i obszarach podmokłych. Główną drogą przenoszenia są zarodniki uwalniane przez grzyby.

## Zabiegi:
Wprawny uzdrowiciel może użyć zaklęć oczyszczających, aby oczyścić organizm z infekcji grzybiczej. Rzadkie zioła występujące w mistycznych gajach można stosować w okładach, aby wspomóc regenerację.