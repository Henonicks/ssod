# Příznaky:
Přetrvávající kašel, potíže s dýcháním a skřípavý zvuk při vdechování. Postižení jedinci trpí oslabenou funkcí plic a může se u nich rozvinout zápal plic.

## Příčiny:
Vdechování vzdušných spor uvolňovaných určitými toxickými rostlinami a houbami, které se nacházejí v temných lesích. Expozice je často způsobena dlouhodobým pobytem v takových prostředích.

## Léčba:
Léčebná kouzla, která posilují dýchací funkce, mohou pomoci zotavení. Vzácná bylina Moonshade, pěstovaná ve specifických alchymistických zahradách, je známá svou účinností při léčbě plicních onemocnění.