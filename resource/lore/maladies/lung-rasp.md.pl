# Objawy:
Uporczywy kaszel, trudności w oddychaniu i chrapliwy dźwięk podczas wdychania. Osoby dotknięte chorobą mają osłabioną czynność płuc i mogą rozwinąć się zapalenie płuc.

## Powoduje:
Wdychanie unoszących się w powietrzu zarodników uwalnianych przez niektóre toksyczne rośliny i grzyby występujące w ciemnych lasach. Narażenie jest często spowodowane długotrwałym przebywaniem w takich środowiskach.

## Zabiegi:
Zaklęcia lecznicze wzmacniające funkcje oddechowe mogą pomóc w powrocie do zdrowia. Rzadkie zioło Moonshade, uprawiane w specjalnych ogrodach alchemicznych, znane jest ze swojej mocy w leczeniu dolegliwości związanych z płucami.